// Returns glyph for ch, falling back to space.
[[nodiscard]] inline const glyph_t& get(char ch) noexcept {
    auto idx = static_cast<unsigned char>(ch);
    if (idx < 0x20 || idx > 0x7E) idx = 0x20;
    return glyphs[idx - 0x20];
}

// Pixel width of a string rendered with this font.
[[nodiscard]] inline int string_pixel_width(std::string_view s) noexcept {
    if (s.empty()) return 0;
    return static_cast<int>(s.size()) * (glyph_w + char_spacing) - char_spacing;
}

